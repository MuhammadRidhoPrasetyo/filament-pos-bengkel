<?php

namespace App\Filament\Clusters\Purchases\Resources\PurchaseItems;

use App\Filament\Clusters\Purchases\PurchasesCluster;
use App\Filament\Clusters\Purchases\Resources\PurchaseItems\Pages\CreatePurchaseItem;
use App\Filament\Clusters\Purchases\Resources\PurchaseItems\Pages\EditPurchaseItem;
use App\Filament\Clusters\Purchases\Resources\PurchaseItems\Pages\ListPurchaseItems;
use App\Filament\Clusters\Purchases\Resources\PurchaseItems\Pages\ViewPurchaseItem;
use App\Filament\Clusters\Purchases\Resources\PurchaseItems\Schemas\PurchaseItemForm;
use App\Filament\Clusters\Purchases\Resources\PurchaseItems\Schemas\PurchaseItemInfolist;
use App\Filament\Clusters\Purchases\Resources\PurchaseItems\Tables\PurchaseItemsTable;
use App\Models\PurchaseItem;
use BackedEnum;
use Filament\Resources\Resource;
use Filament\Schemas\Schema;
use Filament\Support\Icons\Heroicon;
use Filament\Tables\Table;

class PurchaseItemResource extends Resource
{
    protected static ?string $model = PurchaseItem::class;

    protected static string|BackedEnum|null $navigationIcon = Heroicon::OutlinedRectangleStack;
    protected static ?int $navigationSort = 2;
    protected static ?string $navigationLabel = 'Barang Masuk';
    protected static ?string $modelLabel = 'Barang Masuk';
    protected static ?string $pluralModelLabel = 'Barang Masuk';

    protected static ?string $cluster = PurchasesCluster::class;

    public static function form(Schema $schema): Schema
    {
        return PurchaseItemForm::configure($schema);
    }

    public static function infolist(Schema $schema): Schema
    {
        return PurchaseItemInfolist::configure($schema);
    }

    public static function table(Table $table): Table
    {
        return PurchaseItemsTable::configure($table);
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => ListPurchaseItems::route('/'),
            'create' => CreatePurchaseItem::route('/create'),
            'view' => ViewPurchaseItem::route('/{record}'),
            'edit' => EditPurchaseItem::route('/{record}/edit'),
        ];
    }
}
